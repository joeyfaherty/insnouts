package insnouts;

public class Expenses {
	
	
	private long salary;
	
	private long rent;
	
	private long gas;
	
	private long electricity;
	
	private long food;
	
	private long petrol;

	public long getSalary() {
		return salary;
	}

	public void setSalary(long salary) {
		this.salary = salary;
	}

	public long getRent() {
		return rent;
	}

	public void setRent(long rent) {
		this.rent = rent;
	}

	public long getGas() {
		return gas;
	}

	public void setGas(long gas) {
		this.gas = gas;
	}

	public long getElectricity() {
		return electricity;
	}

	public void setElectricity(long electricity) {
		this.electricity = electricity;
	}

	public long getFood() {
		return food;
	}

	public void setFood(long food) {
		this.food = food;
	}

	public long getPetrol() {
		return petrol;
	}

	public void setPetrol(long petrol) {
		this.petrol = petrol;
	}
	
}
